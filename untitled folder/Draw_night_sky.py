import turtle
turtle.bgcolor("black")
t=turtle.Turtle()
t.up
t.goto(-300, 200)
t.down()
t.color("white")
t.begin_fill()
t.fd(30)
t.left(144)
t.fd(30)
t.left(144)
t.fd(30)
t.left(144)
t.fd(30)
t.left(144)
t.fd(30)
t.left(144)
t.end_fill()

t.up()
t.goto(150, 100)
t.down()
t.color("yellow")
t.begin_fill()
t.circle(100, 120)

t.end_fill()
