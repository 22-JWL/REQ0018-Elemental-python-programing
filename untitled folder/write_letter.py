import turtle
turtle.bgcolor("pink")
t= turtle.Turtle()
t.up()
t.back(200)
t.down()
t.color("red")
t.begin_fill()
t.circle(40, 360, 3)
t.end_fill()
t.down()

t.up()
t.fd(100)
t.down()
t.color("blue")
t.begin_fill()
t.circle(40, 360, 4)
t.end_fill()
t.down()

t.up()
t.fd(100)
t.down()
t.color("green")
t.begin_fill()
t.circle(40, 360, 5)
t.end_fill()

t.up()
t.goto(-100, 100)
t.write("도형 색칠하기", font=('굴림', 20, 'bold', 'underline'))

input("press any key to exit")